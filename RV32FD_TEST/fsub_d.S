#include "myriscv_tests.h"

    .text
    .globl main
    .type main, @function
main:
    // 2.5 = 2.5 - 1.0
    TEST_FP_OP2_D(fsub.d, 0x3FF80000, 0x00000000, 0x40040000, 0x00000000, 0x3FF00000, 0x00000000)
    PRINT_IF_ERR(5, a0)
    // -1234.0 = -1235.1 - (-1.1)
    TEST_FP_OP2_D(fsub.d, 0xC0934800, 0x00000000, 0xC0934C66, 0x66666666, 0xBFF19999, 0x9999999A)
    PRINT_IF_ERR(6, a0)
    // 3.1415926400000001 = 3.14159265 - 0.00000001
    TEST_FP_OP2_D(fsub.d, 0x400921FB, 0x52713C03, 0x400921FB, 0x53C8D4F1, 0x3E45798E, 0xE2308C3A)
    PRINT_IF_ERR(7, a0)
    // Canonical NaN = Inf - Inf
    TEST_FP_OP2_D(fsub.d, 0x7FF80000, 0x00000000, 0x7FF00000, 0x00000000, 0x7FF00000, 0x00000000)
    PRINT_IF_ERR(11, a0)

EXIT:
    j EXIT